public class typeno {

    public static void main( String [] args){
        int num = -5 ;

        if ( num == 0) {
            System.out.println("It is zero.");
        }
        else if ( num > 0) {
            System.out.println(num+" is positive");
            }
        else {
            System.out.println(num+" is negative");
        }
    }
}

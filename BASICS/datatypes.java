package BASICS;

public class datatypes {
    public static void main( String [] args){

        /* naming conventions
        java.awt ;
        java.io ;
        javax.swing ;
         */

        // integer data types

        byte rno = 10;                        // 1 byte
        int a = 9 ;                           // 4 bytes
        short b = -1000 ;                     // 2 bytes
        long c = 90000000 ;                   // 8 bytes

        // float data types

        float PI = 3.142F ;                        // 4 byte
        double distance = 1.98e8 ;                 // 8 bytes

        // character data types

        char ch = 'X' ;                             // 2 bytes

        // string data types

        String str = "Komal Harshita" ;

        // boolean data types

        boolean response = true ;                  // 1 bit

        // literals

        boolean result = false ;
        char gender = 'F' ;
        short s = 10000 ;
        int i = -1245 ;
        int decVal = 26 ;
        int octVal = 032 ;
        int hexVal = 0x1a ;
        double d1 = 123.4 ;
        double d2 = 1.234e2 ;
        float f1 = 123.4f ;

    }
}
